fun operacoes(){

    println("2 + 2 = ${2 + 2}")
    println("2 - 2 = ${2 - 2}")
    println("2 * 2 = ${2 * 2}")
    println("2 / 2 = ${2 / 2}")
    println("10 % 4 = ${10 % 4}")
    println("10 % 2 = ${10 % 2}")

    var numero =  10

    println("numero ++ = ${numero++}")
    println("numero -- = ${numero--}")
    println("++numero = ${++numero}")
    println("--numero = ${--numero}")

    /*
    Há também as opções: +=; -=; \=; *= e %=
     */
}