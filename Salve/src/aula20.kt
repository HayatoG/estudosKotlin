class aula20 {
    fun aula() {
        // Operações

        var numero = 10

        println("Número++ = ${numero++}")
        println("Número-- = ${numero--}")

        println("++Número = ${++numero}")
        println("--Número = ${--numero}")

        numero += 2
        println("Número+= 2 = ${numero}")

        numero -= 2
        println("Número-= 2 = ${numero}")

        numero /= 2
        println("Número/= 2 = ${numero}")

        numero *= 2
        println("Número*= 2 = ${numero}")

        numero %= 3
        println("Número*= 2 = ${numero}")

    }
}